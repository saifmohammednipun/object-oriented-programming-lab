public class String1 {
    public static void main(String[] args) {
        String s= "Java Exercise!";
        System.out.println("Original String = "+ s);
        char c1= s.charAt(0);
        char c2= s.charAt(10);
        System.out.println("The Character at position 0 is "+ c1);
        System.out.println("The Character at position 10 is "+ c2);
    }
}
